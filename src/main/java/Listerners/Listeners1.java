package Listerners;

import io.github.bonigarcia.wdm.WebDriverManager;
import org.openqa.selenium.*;
import org.openqa.selenium.chrome.ChromeDriver;
import org.openqa.selenium.support.events.EventFiringWebDriver;
import org.openqa.selenium.support.events.WebDriverEventListener;

public class Listeners1 {
    public static void main(String[] args) throws NoAlertPresentException, InterruptedException {
        WebDriverManager.chromedriver().setup();

        // Create a ChromeDriver instance
        ChromeDriver driver = new ChromeDriver();

        // Wrap the ChromeDriver with EventFiringWebDriver
        EventFiringWebDriver event_driver = new EventFiringWebDriver(driver);

        // Create an instance of your custom listener
        MyTestListener2 testListener = new MyTestListener2();

        // Register the listener with the EventFiringWebDriver
        event_driver.register(testListener);

        // Maximize the window and navigate to the URL
        event_driver.manage().window().maximize();
        event_driver.get("https://www.amazon.in");

        // Perform an action that triggers an event (e.g., clicking on an element)
        event_driver.findElement(By.id("wrong")).click();
    }
}

